package crm.entity;

public class DictCustSource {
	private Integer dcsid;
	private String  dcsname;
	private String  dcsContent;
	public Integer getDcsid() {
		return dcsid;
	}
	public void setDcsid(Integer dcsid) {
		this.dcsid = dcsid;
	}
	public String getDcsname() {
		return dcsname;
	}
	public void setDcsname(String dcsname) {
		this.dcsname = dcsname;
	}
	public String getDcsContent() {
		return dcsContent;
	}
	public void setDcsContent(String dcsContent) {
		this.dcsContent = dcsContent;
	}
}
